// User functions
#ifdef RGB_MATRIX_ENABLE
bool rgb_matrix_indicators_advanced_user(uint8_t led_min, uint8_t led_max) {

    // layer highlights
    // When layer 4 (WIN_FN) is active, highlight the keys that have a function
    // Note! Highlight fails to turn off when RGB is toggled OFF. BUG of my code!!!!
    // Figure out correct solution
    RGB fnHighlight = {RGB_WHITE} ; // the highlight color
    uint8_t layer = get_highest_layer(layer_state);
    if (layer > WIN_BASE) {
        for (uint8_t row = 0; row < MATRIX_ROWS; ++row) {
            for (uint8_t col = 0; col < MATRIX_COLS; ++col) {
                uint8_t index = g_led_config.matrix_co[row][col];

                // Valid LED on that position?
                if ((index >= led_min) && (index < led_max) && (index != NO_LED)) {
                    // Highlight keys with special functions
                    if (keymap_key_to_keycode(layer, (keypos_t){col,row}) > KC_TRNS) {
                        rgb_matrix_set_color(index, fnHighlight.g, fnHighlight.r, fnHighlight.b);
                    }
                }
            }
        }
    }

    // CAPS_LOCK and NUM_LOCK indicators
    // The LED definitions are in keyboards/keychron/v6/ansi/config.h
    // The functions are copied from keyboards/keychron/v6/v6.c
    // RGB_MATRIX_INDICATOR_SET_COLOR(index, red, green, blue);
#    if defined(CAPS_LOCK_LED_INDEX)
    if (host_keyboard_led_state().caps_lock) {
        RGB_MATRIX_INDICATOR_SET_COLOR(CAPS_LOCK_LED_INDEX, 255, 255, 255);
    } else {
        if (!rgb_matrix_get_flags()) {
            RGB_MATRIX_INDICATOR_SET_COLOR(CAPS_LOCK_LED_INDEX, 0, 0, 0);
        }
    }
#    endif // CAPS_LOCK_LED_INDEX, reversed
#    if defined(NUM_LOCK_LED_INDEX)
    if (!host_keyboard_led_state().num_lock) { //reversed
        RGB_MATRIX_INDICATOR_SET_COLOR(NUM_LOCK_LED_INDEX, 255, 255, 255);
    } else {
        if (!rgb_matrix_get_flags()) {
            RGB_MATRIX_INDICATOR_SET_COLOR(NUM_LOCK_LED_INDEX, 0, 0, 0);
        }
    }
#    endif // NUM_LOCK_LED_INDEX

  // returning false inhibits the v6.c 'rgb_matrix_indicators_advanced_kb' funtion
  return false;
}
#endif // RGB_MATRIX_ENABLE
